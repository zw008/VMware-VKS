# ── 踩坑 #16, #17 — hatchling wheel must include mcp_server ─────────────


def test_pyproject_declares_mcp_server_in_wheel() -> None:
    """v1.5.5 incident: hatchling auto-discovery shipped wheels without
    mcp_server/, breaking <skill>-mcp entry point on every install."""
    pyproject = REPO_ROOT / "pyproject.toml"
    data = tomllib.loads(pyproject.read_text())
    packages = (
        data.get("tool", {}).get("hatch", {})
        .get("build", {}).get("targets", {})
        .get("wheel", {}).get("packages", [])
    )
    assert PKG_NAME in packages, (
        f"pyproject.toml must list '{PKG_NAME}' under "
        f"[tool.hatch.build.targets.wheel].packages — got {packages}"
    )
    assert "mcp_server" in packages, (
        f"pyproject.toml must list 'mcp_server' under "
        f"[tool.hatch.build.targets.wheel].packages — got {packages}. "
        "Without this, PyPI wheels ship without the MCP server module "
        "(see CLAUDE.md 踩坑 #16)."
    )


# ── 踩坑 #18, #20 — every module must import cleanly ───────────────────


@pytest.mark.parametrize("top_pkg", [PKG_NAME, "mcp_server"])
def test_every_module_imports(top_pkg: str) -> None:
    """v1.5.5 incident: NSX segment_mgmt.py / nat_route_mgmt.py and
    NSX-Security traceflow.py shipped with re.match() but no `import re`.
    Ruff couldn't catch it; only runtime did."""
    errors: list[str] = []
    try:
        m = importlib.import_module(top_pkg)
    except Exception as e:
        pytest.fail(f"top-level import {top_pkg} failed: {type(e).__name__}: {e}")
    for info in pkgutil.walk_packages(m.__path__, prefix=top_pkg + "."):
        # Skip __main__ — importing it executes the module's main entry
        # (asyncio servers, CLI parsers) which we don't want under pytest.
        if info.name.endswith(".__main__"):
            continue
        try:
            importlib.import_module(info.name)
        except Exception as e:
            errors.append(f"{info.name}: {type(e).__name__}: {e}")
    assert not errors, (
        f"Module import failures (likely missing import / typo / signature mismatch):\n"
        + "\n".join(errors)
    )


# ── 踩坑 #19 — CLI param signatures must match ops function signatures ─


def _walk_python_files(root: Path) -> list[Path]:
    return [p for p in root.rglob("*.py") if "__pycache__" not in p.parts]


def test_cli_app_loads_and_help_runs() -> None:
    """If a Typer subcommand is registered against an ops function whose
    signature drifted, --help itself often blows up (NSX v1.5.4 incident)."""
    from typer.testing import CliRunner

    cli = importlib.import_module(f"{PKG_NAME}.cli")
    app = getattr(cli, "app", None)
    assert app is not None, f"{PKG_NAME}.cli must export `app` (Typer instance)"
    result = CliRunner().invoke(app, ["--help"])
    assert result.exit_code == 0, (
        f"`<{SKILL_NAME}> --help` failed (exit={result.exit_code}):\n{result.output}"
    )


# ── 踩坑 #20 — bare names referenced but not imported  ─────────────────


_RUNTIME_NAMES_TO_CHECK = ("re", "yaml", "json", "ipaddress", "socket")


def test_runtime_names_have_imports() -> None:
    """Walk the package AST. If `re.match(` or similar appears in a file
    that doesn't import the module, fail. v1.5.5 NSX/NSX-Security incident:
    re.match() called without `import re` — ruff missed it."""
    pkg_dir = REPO_ROOT / PKG_NAME
    failures: list[str] = []
    for py in _walk_python_files(pkg_dir):
        src = py.read_text()
        try:
            tree = ast.parse(src)
        except SyntaxError:
            continue
        imported: set[str] = set()
        for node in ast.walk(tree):
            if isinstance(node, ast.Import):
                for alias in node.names:
                    imported.add(alias.asname or alias.name.split(".")[0])
            elif isinstance(node, ast.ImportFrom) and node.module:
                imported.add(node.module.split(".")[0])
        for name in _RUNTIME_NAMES_TO_CHECK:
            # Match `name.identifier(` — covers attribute access patterns.
            if re.search(rf"\b{name}\.[A-Za-z_]\w*\(", src) and name not in imported:
                failures.append(f"{py.relative_to(REPO_ROOT)}: uses `{name}.…` but never imports {name}")
    assert not failures, "Missing imports detected:\n" + "\n".join(failures)
